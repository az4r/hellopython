a = 5
b = 2

#Dodawanie
print(a + b)
print(type(a + b))

#Odejmowanie
print(a - b)
print(type(a - b))

#Mnożneie
print(a * b)
print(type(a * b))

#Dzielenie (wynik powinien być w postaci liczby zmiennoprzecinkowej - float)
print(a / b)
print(type(a / b))

#Reszta z dzielenia
print(a % b)
print(type(a % b))

#Potęgowanie
print(a ** b)
print(type(a ** b))

#Dodawanie wartości do zmiennej
a = 6
a = a + 1
print(a)
#lub
a = 6
a += 1
print(a)

#Dzielenie zmiennej przez wartość
a = 6
a = a / 4
print(a)
#lub
a = 6
a /= 4
print(a)